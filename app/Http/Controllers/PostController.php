    /**
     * Display a listing of the resource.
     *
     * @return \Illuminate\Http\Response
     */
    public function index()
    {
        $user =  auth('sanctum')->user();

        if ( Gate::allows('isAdmin')) {
            $posts = Post::all();
        
            return json_encode($posts);
        }
        else{
            $posts = Post::where('user_id', "$user->id")->get();
        
            return json_encode($posts);
        }
        
    }

